# ── 6. Migration status table ────────────────────────────────────────────────
section("6 / INFO / MIGRATE — migration status table  (colored State column)")

_STATE_STYLE = {
    "success": "bold green",
    "failed": "bold red",
    "pending": "yellow",
    "baseline": "dim",
    "ignored": "dim",
    "missing": "bold red",
}


def state_cell(label: str) -> Text:
    t = Text(label)
    style = _STATE_STYLE.get(label.lower(), "")
    if style:
        t.stylize(style)
    return t


table = Table(box=box.ROUNDED, show_header=True, header_style="bold")
for col, justify in [
    ("Version", "left"),
    ("Description", "left"),
    ("Type", "left"),
    ("Installed On", "left"),
    ("State", "left"),
    ("Exec Time", "right"),
]:
    table.add_column(col, justify=justify)

rows_data = [
    ("1", "baseline", "SQL", "2025-01-10 09:00", "Baseline", "12 ms"),
    ("2.1", "add users table", "SQL", "2025-03-04 14:22", "Success", "87 ms"),
    ("2.2", "add orders table", "SQL", "2025-03-04 14:23", "Success", "134 ms"),
    ("3.1", "seed reference data", "SQL", "2025-04-01 11:05", "Success", "3,201 ms"),
    ("3.1.1", "backport hotfix", "SQL", "2025-04-02 08:00", "Failed", "522 ms"),
    ("3.2.1", "add payment method", "SQL", "", "Pending", ""),
    ("3.2.2", "add fulfilled at", "SQL", "", "Pending", ""),
]
for ver, desc, typ, installed, state, exec_time in rows_data:
    table.add_row(ver, desc, typ, installed, state_cell(state), exec_time)

console.print(table)
console.print(f"Total migrations: {len(rows_data)}  |  Applied: 4  |  Pending: 2  |  Failed: 1")

# ── 7. Migrate: progress bar ─────────────────────────────────────────────────
section("7 / MIGRATE — progress bar  (rich.progress)")

pending = ["V3.2.1__add_payment_method.sql", "V3.2.2__add_fulfilled_at.sql"]

with Progress(
    SpinnerColumn(),
    TextColumn("[progress.description]{task.description}"),
    BarColumn(),
    TaskProgressColumn(),
    TextColumn("[dim]{task.fields[status]}[/dim]"),
    console=console,
    transient=False,
) as progress:
    task = progress.add_task("Applying migrations", total=len(pending), status="")
    for migration in pending:
        progress.update(task, status=f"→ {migration}")
        time.sleep(0.6)
        progress.advance(task)
    progress.update(task, status="done")

console.print()
console.print("[log.notice]NOTICE  2 migrations applied — database is up to date[/log.notice]")


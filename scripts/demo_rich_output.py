DBLIFT_THEME = Theme(
    {
        "log.debug": "dim",
        "log.info": "default",
        "log.warn": "yellow",
        "log.error": "bold red",
        "log.notice": "bold green",
    }
)

console = Console(theme=DBLIFT_THEME, highlight=False, soft_wrap=True, emoji=False, markup=True)

SECTION = "[bold cyan]━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━[/bold cyan]"


def section(title: str) -> None:
    console.print()
    console.print(SECTION)
    console.print(f"[bold white]  {title}[/bold white]")
    console.print(SECTION)
    console.print()


